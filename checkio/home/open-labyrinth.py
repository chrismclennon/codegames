import copy

class Path:
    _DIRECTION = {(1, 0): 'S', (-1, 0): 'N', (0, -1): 'W', (0, 1): 'E'}

    def __init__(self, path=[(1, 1)], directions=''):
        self._path = path[:]
        self._directions = directions

    def __len__(self):
        return len(self._path)

    def current_coordinate(self):
        return self._path[-1] 

    def add_coordinate(self, coordinate):
        if coordinate in self._path:
            return False
        current_coordinate = self.current_coordinate()
        self._directions += self._DIRECTION[tuple(x-y for x, y in zip(coordinate, current_coordinate))]
        self._path.append(coordinate)
        return True

    def coordinates_adjacent_to_current(self):
        current_coordinate = self.current_coordinate()
        adjacent_coordinates = []
        for direction in self._DIRECTION.keys():
            adjacent_coordinates.append(tuple(x+y for x, y in zip(current_coordinate, direction))) 
        return adjacent_coordinates

def checkio(maze_map):
    paths = [Path()]
    while paths:
        new_paths = []
        for current_path in paths:
            current_coordinate = current_path.current_coordinate()
            valid_adjacent_locations = [coordinate for coordinate in current_path.coordinates_adjacent_to_current() \
                                       if maze_map[coordinate[0]][coordinate[1]] == 0]
            for new_loc in valid_adjacent_locations:
                new_path = copy.deepcopy(current_path)
                if new_path.add_coordinate(new_loc):
                    if new_loc == (10, 10):
                        return new_path._directions
                    check_paths = [check_path._path for check_path in new_paths]
                    new_path_current_coordinate = new_path.current_coordinate()
                    if not any(new_path_current_coordinate in check_path for check_path in check_paths) or len(new_path) <= 1:
                        new_paths.append(new_path)
        paths = new_paths
    return False

